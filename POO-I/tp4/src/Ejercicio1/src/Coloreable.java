/**
 * Interfaz para objetos que pueden ser coloreados.
 * El método colorear recibe un color y lo aplica (simulado mediante impresión en consola).
 */
public interface Coloreable {
    /**
     * Aplica el color dado a la figura (impresión en consola).
     * @param color nombre del color a aplicar
     */
    void colorear(String color);
}
